/**
 * L1-036 A乘以B （5 分）
 * 看我没骗你吧 —— 这是一道你可以在 10 秒内完成的题：给定两个绝对值不超过 100 的整数 A 和 B，输出 A 乘以 B 的值。
 */
@Suppress("UnusedMainParameter")
